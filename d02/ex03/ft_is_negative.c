int		ft_putchar(char c);

void	ft_is_negative(int n)
{
	int	a;
	int	b;

	a = 'P';
	b = 'N';
	if (0 <= n)
	{
		ft_putchar(a);
	}
	else
	{
		ft_putchar(b);
	}
}

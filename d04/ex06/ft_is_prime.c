int		ft_is_prime(int nb)
{
	int	i;

	i = 2;
	if (nb <= 1 || nb == 4)
		return (0);
	if (nb == 2 || nb == 3 || nb == 5)
		return (1);
	if (nb > 1)
	{
		while (i != (nb / 2))
		{
			if (nb % i == 0)
				return (0);
			i++;
		}
	}
	return (1);
}

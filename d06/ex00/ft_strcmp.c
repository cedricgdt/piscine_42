int		ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	j;

	i = 0;
	while ((s1[i] == s2[i]) && (s1[i] || s2[i]) != '\0')
		i++;
	if (s1[i] > s2[i])
		return (s1[i] - s2[i]);
	if (s2[i] > s1[i])
		return (s1[i] - s2[i]);
	return (0);
}
